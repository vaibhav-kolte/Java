
public class NonPrimitiveJavaType {

	public static void main(String[] args) {
		int aNumber = 9;
		double pi = 3.14;
		String greet = "Good";
		String blank = " ";
		String greetType = "Morning";
		
		System.out.println(greet + blank + greetType);
		
		System.out.println("Value: " + aNumber + pi);
	}

}	
